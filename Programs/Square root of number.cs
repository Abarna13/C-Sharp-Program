/*

Write a program to find square root of number 25

*/


using System;

class MyProgram
{
  static void Main(string[] args)
  {

    double y = 25;

    //Sqrt() takes double datatype as argument

    double y1 = Math.Sqrt(y);

    Console.WriteLine("Square root of " + y + ": " + y1);
  }
}
